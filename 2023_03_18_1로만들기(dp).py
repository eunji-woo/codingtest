x = int(input())

d = [0] * 3001

for i in range(2, x+1): # 반복문 -> bottom up 방식
    d[i] = d[i-1] + 1 # 1을 뺼 때

    if i % 2 == 0: # 2 또는 3 또는 5로 나누어질 때 (d[i]에는 1을 뺄 때 또는 2, 3, 5로 나눠졌을 때 둘 중 하나의 값만 들억나는 것)
        d[i] = min(d[i], d[i//2] + 1)

    if i % 3 == 0:
        d[i] = min(d[i], d[i//3] + 1)

    if i % 5 == 0:
        d[i] = min(d[i], d[i//5] + 1)

print(d[x])

# 계산 과정.. x가 26일 때
