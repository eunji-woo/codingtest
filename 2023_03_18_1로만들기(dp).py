# i가 2일 때,  -> d[2] = d[1]+1 = 0+1=1 이 될 거다
#             -> if i%2에 걸려서 d[2] = min(d[2], d[1]+1)에 걸리고, d[2]의 값은 결국 1이 된다
# i가 3일 때,  -> d[3] = d[2] + 1 = 1 + 1 = 2 가 될 거고
#             -> if i%3에 걸려서 d[3] = min(d[3], 2)에 의해 d[3] = 2가 될 것임
# i가 4일 때,  -> d[4] = d[3] + 1 = 2 + 1 = 3이 될 거고
#             -> if i%2에 걸려서 d[4] = min(d[4], 2)에 의해 d[3] = 2가 될 것임
